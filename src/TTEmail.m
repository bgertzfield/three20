#import "Three20/TTEmail.h"

#import "Three20/TTCorePreprocessorMacros.h"


///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
@implementation TTEmail

@synthesize to      = _to;
@synthesize title   = _title;
@synthesize body    = _body;
@synthesize isHTML  = _isHTML;


///////////////////////////////////////////////////////////////////////////////////////////////////
+ (TTEmail*) email {
  return [[[self alloc] init] autorelease];
}


///////////////////////////////////////////////////////////////////////////////////////////////////
- (void) dealloc {
  TT_RELEASE_SAFELY(_to);
  TT_RELEASE_SAFELY(_title);
  TT_RELEASE_SAFELY(_body);
  [super dealloc];
}


///////////////////////////////////////////////////////////////////////////////////////////////////
- (TTEmail*) applyTo:(NSString*)to {
  self.to = to;
  return self;
}


///////////////////////////////////////////////////////////////////////////////////////////////////
- (TTEmail*) applyTitle:(NSString*)title {
  self.title = title;
  return self;
}


///////////////////////////////////////////////////////////////////////////////////////////////////
- (TTEmail*) applyBody:(NSString*)body {
  self.body = body;
  return self;
}


///////////////////////////////////////////////////////////////////////////////////////////////////
- (TTEmail*) applyIsHTML:(BOOL)isHTML {
  self.isHTML = isHTML;
  return self;
}


@end
